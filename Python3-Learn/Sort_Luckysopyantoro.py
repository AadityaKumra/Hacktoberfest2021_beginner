# List of Integers 
numbers = [1, 3, 4, 2] 
  
# Sorting list of Integers 
numbers.sort() 
  
print(numbers) 
  
# List of Floating point numbers 
decimalnumber = [2.01, 2.00, 3.67, 3.28, 1.68] 
  
# Sorting list of Floating point numbers 
decimalnumber.sort() 
  
print(decimalnumber) 
  
# List of strings 
words = ["Luck", "For", "Luck"] 
  
# Sorting list of strings 
words.sort() 
  
print(words) 